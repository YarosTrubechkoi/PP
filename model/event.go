package model

import "time"

// Event - базовая структура события из Kafka.
//
// Влияние данных на агрегацию:
//
// 1. UserID (string) - ключ группировки. Мы считаем количество событий ПО КАЖДОМУ пользователю.
//    Если бы вместо UserID был, например, Region или ActionType — мы бы группировали по ним.
//    Чем больше уникальных ключей — тем больше записей в агрегации и памяти.
//
// 2. Если бы в событии было поле Amount (float64), можно было бы агрегировать сумму или среднее,
//    а не только count. Тип агрегации зависит от данных:
//    - count: достаточно одного ключа (UserID) — не нужны числовые поля
//    - sum/avg: нужно числовое поле (Amount, Price и т.д.)
//    - min/max: тоже числовое поле
//    - count distinct: нужно второе поле для подсчёта уникальных значений
//
// 3. Timestamp позволяет делать оконную агрегацию (за последний час, за день и т.д.).
//    Без timestamp — агрегация «за всё время».
type Event struct {
	UserID    string    `json:"user_id"`
	Action    string    `json:"action"`
	Timestamp time.Time `json:"timestamp"`
}
